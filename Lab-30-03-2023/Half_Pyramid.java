public class Half_Pyramid {
    	public static void main(String[] args) {
      //loop for row
        
       for(int i=1;i<=3;i++) { 
         //column loop
		    	   for(int j =0;j<i;j++) {
		    		   System.out.print("*");
		    	   }
		    	   System.out.println();
		       }
      
      
      }



}
